package ocp.solucao;

public class Celular {
    private int volume;
    private boolean somenteVibrar;

    public int getVolume() {
        return volume;
    }

    public boolean isSomenteVibrar() {
        return somenteVibrar;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }

    public void setSomenteVibrar(boolean somenteVibrar) {
        this.somenteVibrar = somenteVibrar;
    }
}
